package com.alan.training.model;

/**
 * @author alangabriel
 * 
 */
public class Token {

	private String id;
	private String token;

	/**
	 * @return the id
	 */
	public final String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public final void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the token
	 */
	public final String getToken() {
		return token;
	}

	/**
	 * @param token
	 *            the token to set
	 */
	public final void setToken(String token) {
		this.token = token;
	}

}
